package kr.goott.bridge.deTailPage;

public class SptVO {
	private String proCode;
	private int itemNum;
	private String userMail;
	private String selectOpt;
	private int selectCnt;
	private String cardName;
	private String cardNum;
	private int totalPrice;
	private String payDate;
	private String userImg;
	public String getProCode() {
		return proCode;
	}
	public void setProCode(String proCode) {
		this.proCode = proCode;
	}
	public int getItemNum() {
		return itemNum;
	}
	public void setItemNum(int itemNum) {
		this.itemNum = itemNum;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public String getSelectOpt() {
		return selectOpt;
	}
	public void setSelectOpt(String selectOpt) {
		this.selectOpt = selectOpt;
	}
	public int getSelectCnt() {
		return selectCnt;
	}
	public void setSelectCnt(int selectCnt) {
		this.selectCnt = selectCnt;
	}
	public String getCardName() {
		return cardName;
	}
	public void setCardName(String cardName) {
		this.cardName = cardName;
	}
	public String getCardNum() {
		return cardNum;
	}
	public void setCardNum(String cardNum) {
		this.cardNum = cardNum;
	}
	public int getTotalPrice() {
		return totalPrice;
	}
	public void setTotalPrice(int totalPrice) {
		this.totalPrice = totalPrice;
	}
	public String getPayDate() {
		return payDate;
	}
	public void setPayDate(String payDate) {
		this.payDate = payDate;
	}
	public String getUserImg() {
		return userImg;
	}
	public void setUserImg(String userImg) {
		this.userImg = userImg;
	}
	
}
